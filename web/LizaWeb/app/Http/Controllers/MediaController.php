     */
    public function index()
    {
        $cat_filter= request()->input('categories', []);
        $media=Media::with('categories')->get();
        $media_output=[];
        foreach ($media as $m){
            $cat=[];
            foreach ($m->categories as $c){
                array_push($cat,$c->id);
            }
            if (count(array_intersect($cat_filter, $cat)) === count($cat_filter)){

                array_push($media_output,$m);

            }
        }
        return $media_output;
    }

